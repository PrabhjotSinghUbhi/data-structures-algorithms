package com.prabhjot.java.core.input_output.different_types_of_streams;

public class Streams {
    public static void main(String[] args) {
        /**
         * There are Two types of streams in java:
         * 1. Byte Stream
         * 2. Character Stream
         */
        /**
         * Byte Streams
         * - raw bytes
         * - Classes that end with Stream
         * -- e.g. InputStream, FileInputStream, BufferedInputStreamDemo
         */
        /**
         * Character Stream
         * - works with characters(Unicode)
         * - ends with Reader or Writer
         * - converts Bytes into characters.
         */
    }
}
